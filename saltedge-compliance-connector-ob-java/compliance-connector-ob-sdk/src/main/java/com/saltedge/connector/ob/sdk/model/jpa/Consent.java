package com.saltedge.connector.ob.sdk.model.jpa;

import com.saltedge.connector.ob.sdk.SDKConstants;
import com.saltedge.connector.ob.sdk.provider.dto.account.ObAccountIdentifier;
import com.saltedge.connector.ob.sdk.provider.dto.payment.ObPaymentInitiationData;
import com.saltedge.connector.ob.sdk.provider.dto.payment.ObRiskData;
import org.springframework.util.StringUtils;

import jakarta.persistence.Column;
import jakarta.persistence.Convert;
import jakarta.persistence.Entity;
import jakarta.validation.constraints.Size;
import java.io.Serializable;
import java.time.Instant;
import java.util.List;
import java.util.Objects;

/**
 * Database entity for saving data about connection between Connector and Salt Edge Compliance Solution
 */
@Entity
public class Consent extends BaseJpaEntity implements Serializable {

    @Column(name = "tpp_name")
    @Size(min = 1, max = 4096)
    public String tppName;

    @Column(name = SDKConstants.KEY_CONSENT_ID, nullable = false)
    public String consentId;

    /**
     * Values: AwaitingAuthorisation, Authorised, Rejected, Revoked
     */
    @Column(name = SDKConstants.KEY_STATUS, nullable = false)
    public String status;

    @Column(name = SDKConstants.KEY_AUTHORIZATION_ID)
    public String authorizationId;

    @Column(name = SDKConstants.KEY_AUTH_CODE)
    public String authCode;

    @Column(name = SDKConstants.KEY_ACCESS_TOKEN)
    public String accessToken;

    @Column(name = SDKConstants.KEY_USER_ID)
    public String userId;

    //Identifiers with granted access for PIS
    @Column(name = "account_identifiers")
    @Convert(converter = StringListConverter.class)
    public List<String> accountIdentifiers;

    //AIS initial data
    @Column(name = "permissions", length = 4096)
    @Convert(converter = StringListConverter.class)
    public List<String> permissions;

    @Column(name = "permissions_expires_at")
    public Instant permissionsExpiresAt;

    @Column(name = "transaction_from_date_time")
    public Instant transactionFrom;

    @Column(name = "transaction_to_date_time")
    public Instant transactionTo;

    //PIS initial data
    @Column(name = "payment", length = 65536)
    @Convert(converter = PaymentInitiationDataConverter.class)
    public ObPaymentInitiationData paymentInitiation;

    @Column(name = "payment_type")
    public String paymentType;

    @Column(name = "risk", length = 65536)
    @Convert(converter = PaymentRiskDataConverter.class)
    public ObRiskData risk;

    @Column(name = "compliance_payment_id")
    public String compliancePaymentId;

    @Column(name = "payment_id")
    public String paymentId;

    //PIIS initial data
    @Column(name = "debtor_account", length = 4096)
    @Convert(converter = AccountIdentifierConverter.class)
    public ObAccountIdentifier debtorAccount;

    public Consent() {
    }

    public Consent(String tppName, String consentId, String status) {
        this.tppName = tppName;
        this.consentId = consentId;
        this.status = status;
    }

    public static Consent createAisConsent(
      String tppName,
      String consentId,
      String status,
      List<String> permissions,
      Instant permissionsExpiresAt,
      Instant transactionFrom,
      Instant transactionTo
    ) {
        Consent consent = new Consent(tppName, consentId, status);
        consent.permissions = permissions;
        consent.permissionsExpiresAt = permissionsExpiresAt;
        consent.transactionFrom = transactionFrom;
        consent.transactionTo = transactionTo;
        return consent;
    }

    public static Consent createPisConsent(
      String tppName,
      String consentId,
      String status,
      String paymentType,
      ObPaymentInitiationData paymentInitiation,
      ObRiskData risk
    ) {
        Consent consent = new Consent(tppName, consentId, status);
        consent.paymentType = paymentType;
        consent.paymentInitiation = paymentInitiation;
        consent.risk = risk;
        return consent;
    }

    public static Consent createPiisConsent(
      String tppName,
      String consentId,
      String status,
      Instant permissionsExpiresAt,
      ObAccountIdentifier debtorAccount
    ) {
        Consent consent = new Consent(tppName, consentId, status);
        consent.permissionsExpiresAt = permissionsExpiresAt;
        consent.debtorAccount = debtorAccount;
        return consent;
    }

    public boolean isAwaitingAuthorisation() {
        return "AwaitingAuthorisation".equalsIgnoreCase(status);
    }

    public boolean isAuthorised() {
        return ("Authorised".equalsIgnoreCase(status) || "approved".equalsIgnoreCase(status))
          && StringUtils.hasText(userId);
    }

    public boolean isConsumed() {
        return "Consumed".equalsIgnoreCase(status);
    }

    public boolean isRejected() {
        return "Rejected".equalsIgnoreCase(status) || "denied".equalsIgnoreCase(status);
    }

    public boolean isRevoked() {
        return "Revoked".equalsIgnoreCase(status);
    }

    public boolean isAisConsent() {
        return permissions != null;
    }

    public boolean isPisConsent() {
        return paymentInitiation != null;
    }

    public boolean isPiisConsent() {
        return debtorAccount != null;
    }

    public boolean aisPermissionsExpired() {
        return isAisConsent() && permissionsExpiresAt != null && permissionsExpiresAt.isBefore(Instant.now());
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Consent consent = (Consent) o;
        return Objects.equals(tppName, consent.tppName) && Objects.equals(consentId, consent.consentId) && Objects.equals(status, consent.status) && Objects.equals(authorizationId, consent.authorizationId) && Objects.equals(authCode, consent.authCode) && Objects.equals(accessToken, consent.accessToken) && Objects.equals(userId, consent.userId) && Objects.equals(accountIdentifiers, consent.accountIdentifiers) && Objects.equals(permissions, consent.permissions) && Objects.equals(permissionsExpiresAt, consent.permissionsExpiresAt) && Objects.equals(transactionFrom, consent.transactionFrom) && Objects.equals(transactionTo, consent.transactionTo) && Objects.equals(paymentInitiation, consent.paymentInitiation) && Objects.equals(paymentType, consent.paymentType) && Objects.equals(risk, consent.risk) && Objects.equals(compliancePaymentId, consent.compliancePaymentId) && Objects.equals(paymentId, consent.paymentId) && Objects.equals(debtorAccount, consent.debtorAccount);
    }

    @Override
    public int hashCode() {
        return Objects.hash(tppName, consentId, status, authorizationId, authCode, accessToken, userId, accountIdentifiers, permissions, permissionsExpiresAt, transactionFrom, transactionTo, paymentInitiation, paymentType, risk, compliancePaymentId, paymentId, debtorAccount);
    }

    @Override
    public String toString() {
        return "Consent{" +
            "id=" + id +
            ", tppName='" + tppName + '\'' +
            ", consentId='" + consentId + '\'' +
            ", status='" + status + '\'' +
            ", authorizationId='" + authorizationId + '\'' +
            ", authCode='" + authCode + '\'' +
            ", accessToken='" + accessToken + '\'' +
            ", userId='" + userId + '\'' +
            ", accountIdentifiers=" + accountIdentifiers +
            ", permissions=" + permissions +
            ", permissionsExpiresAt=" + permissionsExpiresAt +
            ", transactionFrom=" + transactionFrom +
            ", transactionTo=" + transactionTo +
            ", paymentInitiation=" + paymentInitiation +
            ", paymentType='" + paymentType + '\'' +
            ", risk=" + risk +
            ", compliancePaymentId='" + compliancePaymentId + '\'' +
            ", paymentId='" + paymentId + '\'' +
            ", debtorAccount=" + debtorAccount +
            '}';
    }
}
